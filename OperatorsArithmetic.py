#Arithmetic Operators

num1=10
num2=5

print(num1 + num2) #Addition of two numbers
print(num1 - num2) #Subtraction of two numbers
print(num1 * num2) #Multiplication of teo numbers
print(num1 / num2) #Division of two numbers
print(num1 % num2) #print the reminder of two numbers
print(num1 ** num2) #power of number
print(num1 // num2) #print the integer of division number